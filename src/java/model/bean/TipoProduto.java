package model.bean;

/**
 *
 * @author paulo
 */
public class TipoProduto {
    private int id;
    private String nomeCategoria;
    private int valor;


    public TipoProduto() {
    }

    public TipoProduto(int id, String nomeCategoria, int valor) {
        this.id = id;
        this.nomeCategoria = nomeCategoria;
        this.valor = valor;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNomeCategoria() {
        return nomeCategoria;
    }

    public void setNomeCategoria(String nomeCategoria) {
        this.nomeCategoria = nomeCategoria;
    }

    public int getValor() {
        return valor;
    }

    public void setValor(int valor) {
        this.valor = valor;
    }

    
    
    
    
}
